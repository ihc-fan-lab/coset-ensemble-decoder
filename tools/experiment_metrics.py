def format_error_label(value):
    """格式化错误率标签为科学计数法"""
    if value == 0:
        return "0"
    elif value < 0.01:
        return f"{value:.3e}"
    else:
        return f"{value:.4f}"


def get_fidelity(LER, decoding_latency, d=1):
    """计算系统保真度"""
    if LER <= 0:
        return 1.0
    system_fidelity = (1 - 2 * LER) ** (decoding_latency / d)
    return max(0.0, system_fidelity)


